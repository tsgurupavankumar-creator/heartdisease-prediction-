from reportlab.lib.pagesizes import letter
from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Image, Table, TableStyle
from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib import colors
import os
import datetime

def generate_pdf_report(metrics: dict, feature_importance_img: str = None, roc_img: str = None, output_path: str = "reports/clinical_report.pdf"):
    """Generates a professional PDF report using ReportLab."""
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    
    doc = SimpleDocTemplate(output_path, pagesize=letter,
                            rightMargin=72, leftMargin=72,
                            topMargin=72, bottomMargin=18)
    
    styles = getSampleStyleSheet()
    
    # Custom Styles
    title_style = ParagraphStyle(
        'CustomTitle',
        parent=styles['Heading1'],
        fontSize=24,
        textColor=colors.HexColor('#1E88E5'),
        spaceAfter=30,
        alignment=1 # Center
    )
    
    heading_style = ParagraphStyle(
        'CustomHeading',
        parent=styles['Heading2'],
        fontSize=18,
        textColor=colors.HexColor('#2C3E50'),
        spaceAfter=12,
        spaceBefore=20
    )
    
    normal_style = styles["Normal"]
    
    Story = []
    
    # 1. Cover Page / Title
    Story.append(Paragraph("Healthcare Analytics Platform", title_style))
    Story.append(Paragraph("Heart Disease Risk Prediction Report", title_style))
    Story.append(Spacer(1, 2 * 72))
    
    Story.append(Paragraph(f"<b>Generated Date:</b> {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}", normal_style))
    Story.append(Spacer(1, 4 * 72))
    
    # Disclaimer
    disclaimer = """<b>Disclaimer:</b> This report is generated by an automated Machine Learning pipeline for analytical purposes. 
    It is not a substitute for professional medical advice, diagnosis, or treatment."""
    Story.append(Paragraph(disclaimer, styles["Italic"]))
    
    Story.append(Spacer(1, 2 * 72))
    Story.append(Paragraph("Confidential", styles["Normal"]))
    
    # Page Break
    # ReportLab SimpleDocTemplate doesn't strictly need PageBreak unless we use PageBreak object, 
    # but let's just append lots of spacer or we can import PageBreak
    from reportlab.platypus import PageBreak
    Story.append(PageBreak())
    
    # 2. Executive Summary
    Story.append(Paragraph("Executive Summary", heading_style))
    Story.append(Paragraph(
        "This document provides a comprehensive overview of the trained machine learning model for heart disease risk prediction. "
        "The model is optimized prioritizing Recall and ROC-AUC to minimize false negatives in clinical settings.",
        normal_style
    ))
    
    # 3. Model Performance
    Story.append(Paragraph("Model Performance Metrics", heading_style))
    
    data = [
        ["Metric", "Value"],
        ["Accuracy", f"{metrics.get('Accuracy', 0):.4f}"],
        ["Precision", f"{metrics.get('Precision', 0):.4f}"],
        ["Recall (Sensitivity)", f"{metrics.get('Recall', 0):.4f}"],
        ["F1 Score", f"{metrics.get('F1 Score', 0):.4f}"],
        ["ROC-AUC Score", f"{metrics.get('ROC-AUC', 0):.4f}"]
    ]
    
    t = Table(data, colWidths=[200, 200])
    t.setStyle(TableStyle([
        ('BACKGROUND', (0,0), (1,0), colors.HexColor('#1E88E5')),
        ('TEXTCOLOR', (0,0), (1,0), colors.whitesmoke),
        ('ALIGN', (0,0), (-1,-1), 'CENTER'),
        ('FONTNAME', (0,0), (-1,0), 'Helvetica-Bold'),
        ('BOTTOMPADDING', (0,0), (-1,0), 12),
        ('BACKGROUND', (0,1), (-1,-1), colors.HexColor('#F5F9FC')),
        ('GRID', (0,0), (-1,-1), 1, colors.HexColor('#E0E6ED'))
    ]))
    Story.append(t)
    Story.append(Spacer(1, 20))
    
    # Note on Model Selection
    Story.append(Paragraph(
        "<b>Note:</b> In healthcare applications, a high Recall is critical as it indicates the model's ability to identify all true positive cases.",
        normal_style
    ))
    
    Story.append(PageBreak())
    
    # 4. Visualizations (if provided)
    if roc_img and os.path.exists(roc_img):
        Story.append(Paragraph("Receiver Operating Characteristic (ROC)", heading_style))
        Story.append(Image(roc_img, width=400, height=300))
        Story.append(Spacer(1, 20))
        
    if feature_importance_img and os.path.exists(feature_importance_img):
        Story.append(Paragraph("Feature Importance", heading_style))
        Story.append(Image(feature_importance_img, width=400, height=300))
        Story.append(Spacer(1, 20))
    
    # 5. Recommendations
    Story.append(Paragraph("Recommendations", heading_style))
    Story.append(Paragraph(
        "Based on the feature importance analysis, clinicians should closely monitor the top contributing factors when assessing patient risk.",
        normal_style
    ))
    
    # Build
    doc.build(Story)
    return output_path
